    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa loại thức uống này không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
        {
            String kq = QLLoaiNuoc.deleteLoaiNuoc(txtMaLoai.getText());
            JOptionPane.showConfirmDialog(null, kq, "Oops...", JOptionPane.DEFAULT_OPTION);
            modelList.removeAllElements();
            loadListLoaiNuoc();
            btn_Them.setEnabled(true);
            btn_Sua.setEnabled(true);
            btn_Xoa.setEnabled(true);
            txtMaLoai.setText("");
            txtTenLoai.setText("");
            Load_Combobox_TenLoaiNuoc();
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

