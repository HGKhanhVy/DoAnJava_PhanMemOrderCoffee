    private void loadListLoaiNuoc()
    {
        for (String tenLoaiNuoc : QLLoaiNuoc.getTenLoai()) 
        {
            modelList.addElement(tenLoaiNuoc);
        }
        listLoaiNuoc.setModel(modelList);
    }
    
    private void Load_Combobox_TenLoaiNuoc()
    {
        dsTenLoaiNuoc = QLLoaiNuoc.getTenLoai();
        cbo_TenLoaiNuoc.removeAllItems();
        for (String item : dsTenLoaiNuoc)
            cbo_TenLoaiNuoc.addItem(item);
    }
    
    private void LoadDataThucUong()
    {
        modelNuoc.setColumnIdentifiers(QLNuoc.showTitle());
        modelNuoc.setRowCount(0);
        for (THUCUONG_DTO nuoc : QLNuoc.getALL()) 
        {
            modelNuoc.addRow(new Object[]{
                nuoc.getMANUOC(), nuoc.getTENNUOC(), nuoc.getSIZE(), nuoc.getGIA()});
        }
        JTable_Nuoc.setModel(modelNuoc);
    }
    
    private void SearchLoaiNuoc()
    {
        txtTenLoai.setText(listLoaiNuoc.getSelectedValue());
        txtMaLoai.setText(QLLoaiNuoc.getMaLoai(listLoaiNuoc.getSelectedValue()));
    }
    
