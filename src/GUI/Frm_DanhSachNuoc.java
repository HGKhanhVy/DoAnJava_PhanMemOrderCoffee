    public String getMaLoaiThucUong() {
        String maLoaiNuoc = "L";
        int sl = QLLoaiNuoc.getSoLuongLoai();
        sl += 1;
        if(sl < 10)
            maLoaiNuoc = "L00" + sl;
        else
            maLoaiNuoc = "L0" + sl;
        return maLoaiNuoc;
    }
