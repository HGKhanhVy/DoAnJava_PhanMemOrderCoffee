    private void SearchNuoc()
    {
        int index = JTable_Nuoc.getSelectedRow();
        THUCUONG_DTO nuoc = new THUCUONG_DTO();
        QLNuoc.LoadNuoc(index, nuoc);
        cbo_TenLoaiNuoc.setSelectedItem(QLLoaiNuoc.GetTenLoaiNuoc(QLNuoc.GetMaLoaiNuoc(nuoc.getMANUOC())));
        txt_MaNuoc.setText(nuoc.getMANUOC());
        txt_TenNuoc.setText(nuoc.getTENNUOC());
        txt_DonGia.setText(nuoc.getGIA() + "");
        txt_Size.setText(nuoc.getSIZE());
        txt_TrangThai.setText(QLNuoc.GetTrangThai(nuoc.getMANUOC()));
    }
