    private void btn_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimKiemActionPerformed
        if(txt_TenNuocCanTim.getText().equals(""))
        {
            JOptionPane.showConfirmDialog(null, "Hãy nhập tên nước cần tìm!", "Oops...", JOptionPane.DEFAULT_OPTION);
            return;
        }
        String announ = QLNuoc.CheckExist(txt_TenNuocCanTim.getText());
        JOptionPane.showConfirmDialog(null, announ, "Oops...", JOptionPane.DEFAULT_OPTION);
        int rs = QLNuoc.SearchNuoc(txt_TenNuocCanTim.getText());
        JTable_Nuoc.setRowSelectionInterval(rs, rs);
        SearchNuoc();
    }//GEN-LAST:event_btn_TimKiemActionPerformed

    
