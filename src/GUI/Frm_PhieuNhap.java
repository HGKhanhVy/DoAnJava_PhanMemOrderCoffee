        txt_tt.setText(String.valueOf(tien));
        txt_tt.getText();

        String MAPHIEUNHAP, TENPHIEUNHAP, NGAYNHAP, MANHANVIEN, MANHACUNGCAP, MANGUYENLIEU, TENNGUYENLIEU, SOLUONG, DVT, GIA, THANHTIEN, TONGTIEN;

