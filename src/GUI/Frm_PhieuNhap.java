    public void loadTable() {
        tblModel_Phieu.setRowCount(0);
        tblModel_Phieu.setColumnIdentifiers(phieuBLL.showTitle());
        lstPHIEU = phieuBLL.getALL();
        for (PHIEUNHAP_DTO items : lstPHIEU) {
            tblModel_Phieu.addRow(new Object[]{
                // MAPHIEUNHAP, TENPHIEUNHAP,NGAYNHAP, MANHANVIEN, MANHACUNGCAP , MANGUYENLIEU, TENNGUYENLIEU, SOLUONG, DVT, GIA, THANHTIEN, TONGTIEN;
                items.getMAPHIEUNHAP(), items.getTENPHIEUNHAP(), items.getNGAYNHAP(), items.getMANHANVIEN(), items.getMANHACUNGCAP(), items.getMANGUYENLIEU(), items.getTENNGUYENLIEU(), items.getSOLUONG(), items.getDVT(), items.getGIA(), items.getTHANHTIEN()
            });
        }
        tbl_PN.setModel(tblModel_Phieu);
    }
    
