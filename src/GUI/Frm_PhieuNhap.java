    public void tinhtongtien() {
        float sum = 0;
        String maPhieuNhap = txt_maphieu.getText();
        for (int i = 0; i < tbl_PN.getRowCount(); i++) {
            if (tbl_PN.getValueAt(i, 0).toString().equalsIgnoreCase(maPhieuNhap) == true) {
                sum += Float.valueOf(tbl_PN.getValueAt(i, 10).toString());
            }
        }
        txt_tongtien.setText(String.valueOf(sum));
        //tt();
    }
    
    public void XuatReport(String _mapn) {
        try {
            Connect_DB cn = new Connect_DB();
            Hashtable map = new Hashtable();            
            JasperReport rpt = JasperCompileManager.compileReport("D:/DoAnJava_PhanMemOrderCoffee/src/Report/rpt_NhapHang.jrxml");
            map.put("maPN", _mapn);            
            Connection con = cn.getCn();
            JasperPrint p = JasperFillManager.fillReport(rpt, map, con);
            JasperViewer.viewReport(p, false);
        } catch (JRException ex) {
            Logger.getLogger(Panel_ChiTietBan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
