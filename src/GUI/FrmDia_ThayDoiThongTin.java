    private void Load_Combobox_ChucVu()
    {
        dsTenChucVu = QLChucVu.getTenCV();
        cbo_ChucVu.removeAllItems();
        for (String item : dsTenChucVu)
            cbo_ChucVu.addItem(item);
    }
    private void btn_LuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LuuActionPerformed
        String gioiTinh = "";
        if(radio_Nam.isSelected() == true)
            gioiTinh = "Nam";
        if(radio_Nu.isSelected() == true)
            gioiTinh = "Nữ";
        NhanVienDTO nv = new NhanVienDTO(txt_MaNV.getText(), QLChucVu.GetMaChucVu(txt_MaNV.getText()), txt_TenNV.getText(), gioiTinh, txtArea_DiaChi.getText(), txt_Sdt.getText());
        String kq = QLNhanVien.thayDoiThongTin(nv);
        JOptionPane.showConfirmDialog(null, kq, "Oops...", JOptionPane.DEFAULT_OPTION);
    }//GEN-LAST:event_btn_LuuActionPerformed

    private void btn_ThoatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThoatActionPerformed
        int n = JOptionPane.showConfirmDialog(this, "Quay lại trang thông tin ?", "Đăng xuất",JOptionPane.YES_NO_OPTION);
        if( n == JOptionPane.YES_OPTION)
        {
            Frm_ThongTinCaNhan frm = new Frm_ThongTinCaNhan();
            frm.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btn_ThoatActionPerformed

