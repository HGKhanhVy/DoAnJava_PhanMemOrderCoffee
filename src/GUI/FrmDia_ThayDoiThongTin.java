    public void getTaiKhoan(String taiKhoan) {
        tk = taiKhoan;
        txt_MaNV.setText(QLNhanVien.GetMaNV(tk));
        cbo_ChucVu.setSelectedItem(QLNhanVien.GetChucVu(tk));
        txt_TenNV.setText(QLNhanVien.GetNameNV(tk));
        if((QLNhanVien.GetGioiTinh(tk)).equals("Nam"))
            radio_Nam.setSelected(true);
        if((QLNhanVien.GetGioiTinh(tk)).equals("Nữ"))
            radio_Nu.setSelected(true);
        txtArea_DiaChi.setText(QLNhanVien.GetDiaChi(tk));
        txt_Sdt.setText(QLNhanVien.GetSDT(tk));
    }
