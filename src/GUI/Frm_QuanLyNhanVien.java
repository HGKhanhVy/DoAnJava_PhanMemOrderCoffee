    public String getMaNhanVien() {
        String maLoaiNuoc = "NV";
        int sl = QLNhanVien.getSoLuongNhanVien();
        sl += 1;
        if(sl < 10)
            maLoaiNuoc = "NV0" + sl;
        else
            maLoaiNuoc = "NV" + sl;
        return maLoaiNuoc;
    }
