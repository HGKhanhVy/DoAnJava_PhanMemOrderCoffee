    private void ClearAllTxt()
    {
        txt_MaNV.setText("");
        txt_TenNV.setText("");
        txt_Sdt.setText("");
        txt_TaiKhoan.setText("");
        txt_MatKhau.setText("");
        txt_XNMatKhau.setText("");
        txtArea_DiaChi.setText("");
    }
    public Boolean checkAllTxt()
    {
        if(txt_MaNV.getText().equals("") || txt_TenNV.getText().equals("") || txt_Sdt.getText().equals("") || 
            txt_TaiKhoan.getText().equals("") || txt_MatKhau.getText().equals("") || txt_XNMatKhau.getText().equals("") || 
            txtArea_DiaChi.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đầy đủ thông tin", "Thông báo",2);
            return false;
        }
        return true;
    }
    private void SearchNV()
    {
        int index = JTable_NhanVien.getSelectedRow();
        NhanVienDTO nv = new NhanVienDTO();
        QLNhanVien.LoadNV(index, nv);
        txt_MaNV.setText(nv.getMaNV());
        txt_TenNV.setText(nv.getTenNV());
        if(nv.getGioiTinh().equals("Nam"))
            radio_Nam.setSelected(true);
        if(nv.getGioiTinh().equals("Nữ"))
            radio_Nu.setSelected(true);
        txtArea_DiaChi.setText(nv.getDiaChi());
        txt_Sdt.setText(nv.getSdt());
        txt_TaiKhoan.setText(nv.getTaiKhoan());
        txt_MatKhau.setText(nv.getMatKhau());
        txt_XNMatKhau.setText(nv.getMatKhau());
        cbo_ChucVu.setSelectedItem(QLNhanVien.GetChucVu(nv.getTaiKhoan()));
    }
