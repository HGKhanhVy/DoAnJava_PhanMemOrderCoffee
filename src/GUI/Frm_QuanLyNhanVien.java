    private void LoadData() 
    {
        model.setColumnIdentifiers(QLNhanVien.listTitleNV());
        model.setRowCount(0);
        for (NhanVienDTO nv : QLNhanVien.showListNV_BLL()) 
        {
            model.addRow(new Object[]{
                nv.getMaNV(), QLNhanVien.GetChucVu(nv.getTaiKhoan()), nv.getTenNV(), nv.getGioiTinh(), nv.getDiaChi(), nv.getSdt(), nv.getTaiKhoan()});
        }
        JTable_NhanVien.setModel(model);
    }

